    /**
     * Maps a page number (1-604) to the starting surah number, surah name, and ayah number.
     * Returns a Triple(surahNumber, surahName, ayahNumber).
     */
    fun getPageStartingAyah(page: Int): Triple<Int, String, Int> {
        val clamped = page.coerceIn(1, 604)
        val entry = pageMap[clamped] ?: pageMap[1]!!
        return Triple(entry.surahNumber, entry.surahName, entry.ayahNumber)
    }

